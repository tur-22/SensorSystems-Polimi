void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM3) {
		int8_t x_acc, y_acc, z_acc;
		float x_g_acc, y_g_acc, z_g_acc;
		HAL_I2C_Mem_Read(&hi2c1, address, &OUT_X, 1, &x_acc, 1, 50);
		HAL_I2C_Mem_Read(&hi2c1, address, &OUT_Y, 1, &y_acc, 1, 50);
		HAL_I2C_Mem_Read(&hi2c1, address, &OUT_Z, 1, &z_acc, 1, 50);

		x_g_acc = x_acc/64.0;
		y_g_acc = y_acc/64.0;
		z_g_acc = z_acc/64.0;
		length = snprintf(string, sizeof(string),
				"X: %.2f g\nY: %.2f g\nZ: %.2f g\n", x_g_acc, y_g_acc, z_g_acc);
		HAL_UART_Transmit_DMA(&huart2, string, length);
	}
}
